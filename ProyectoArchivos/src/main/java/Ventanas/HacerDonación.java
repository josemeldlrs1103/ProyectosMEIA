    public boolean Encontrado(String usuario, String material, String fecha)
    {
        boolean resultado = false;
        boolean usuarioencontrado = false;
        boolean materialencontrado = false;
        boolean fechaencontrada = false;
        ArrayList<String> lista = new ArrayList<>();
        String ruta = "C:\\MEIA\\DonacionIndex.txt";
        String linea = "";
        File f = new File(ruta);
        try{
            if (!f.exists()) {
                f.createNewFile();
            }
            FileReader fr = new FileReader(ruta);
            BufferedReader br = new BufferedReader(fr);
            linea = br.readLine();
            while(linea!=null){
                lista.add(linea);
                linea = br.readLine();
            }
            for(String x : lista)
            {
                if(x.contains(usuario)){
                    usuarioencontrado = true;
                }
            }
            if (usuarioencontrado) {
                for(String x : lista)
                {
                    if(x.contains(usuario+","+material)){
                        materialencontrado = true;
                    }
                }
            }
            if (materialencontrado){
                for(String x : lista)
                {
                    if(x.contains(usuario+","+material+","+fecha)){
                        fechaencontrada = true;
                    }
                }
            }
            if (fechaencontrada){
                resultado = true;
            }
        }catch(IOException ex){
            Logger.getLogger(HacerDonaci贸n.class.getName()).log(Level.SEVERE, null, ex);
        }
        return  resultado;
    }
    
    public String InsertarIndexadoMaster(String LineaInsertar,int MaxLineas)
    {
        boolean Escrito = false;
        int Bloque = 1;
        int IndiceLinea = 1;
        String ext = ".txt";
        String inicio = "C:\\MEIA\\Donacion_";
        String ruta = inicio + Bloque + ext;
        String linea = "";
        String direccion = "";
        ArrayList<String> lista = new ArrayList<>();
        try {
        while (!Escrito) {
            File f = new File(ruta);
            if (!f.exists()) {
                f.createNewFile();
            }
            FileReader fr = new FileReader(ruta);
            BufferedReader br = new BufferedReader(fr);
            linea = br.readLine();
            while (linea!=null) {
                lista.add(linea);
                IndiceLinea++;
                linea = br.readLine();
            }
            fr.close();
            br.close();
            if (IndiceLinea<=MaxLineas) {
                f.delete();
                f.createNewFile();
                FileWriter fw = new FileWriter(ruta);
                BufferedWriter bw = new BufferedWriter(fw);
                for (String x :lista) {
                    bw.write(x);
                    bw.newLine();
                }
                bw.write(LineaInsertar);
                //fw.close();
                bw.close();
                Escrito = true;
                direccion = Bloque+"."+IndiceLinea;
            }else{
                IndiceLinea = 1;
                lista = new ArrayList<>();
            }
            Bloque++;
            ruta = inicio + Bloque + ext;
        }
        } catch (IOException ex) {
            Logger.getLogger(HacerDonaci贸n.class.getName()).log(Level.SEVERE, null, ex);
        }
        return (direccion);
    }
    
